public class Mahasiswa {
    String nama;
    int umur;
    String NIM;
    String jurusan;
    double IP1;
    double IP2;
    double IP3;

    Mahasiswa(String nama, int umur, String NIM, String jurusan, double IP1, double IP2, double IP3) {
        this.nama = nama;
        this.umur = umur;
        this.NIM = NIM;
        this.jurusan = jurusan;
        this.IP1 = IP1;
        this.IP2 = IP2;
        this.IP3 = IP3;

    }
}